package exam03;

public class ComparionOp { // 비교연산자
	public static void main(String[] args) {
		
		System.out.println(350 < 200);
		System.out.println(350 > 200);
		
		System.out.println(350 <= 200);
		System.out.println(350 >= 200);
		
		System.out.println(350 == 200);
		System.out.println(350 != 200);
		
		boolean isBoolean = (530 >= 270); // 괄호 없어도 되나 가시성을 위해 넣어줌
		System.out.println(isBoolean);
		
	}

}
